    private void guardarDatos() {
        if(jRHabilitar.isSelected()){
            Configuracion.Edad = true;
        }else{
            Configuracion.Edad = false;
        }
        Configuracion.andalucia = jCAndalucia.isSelected();
        Configuracion.castilla = jCCastilla.isSelected();
    }

    private void cargarDatos() {
        if(Configuracion.Edad = true){
            jRHabilitar.setSelected(true);
            
        }else{
            jRDeshabilitar.setSelected(true);
        }
        jCAndalucia.setSelected(Configuracion.andalucia);
        jCAndalucia.setSelected(Configuracion.castilla);
    }
